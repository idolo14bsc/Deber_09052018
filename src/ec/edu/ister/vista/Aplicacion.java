    private void txtGeneroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtGeneroKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        int limite=1;
        if((txtGenero.getText().length()==limite) || Character.isDigit(c))  {
             JOptionPane.showMessageDialog(null,"Ingrese solo un caracter \n Nopuede Ingresar Digitos");
             evt.consume();
            
        }
        
    }//GEN-LAST:event_txtGeneroKeyTyped

    private void txtEdadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEdadKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar(); 
        int edad=2;
        if((txtEdad.getText().length()>=edad) || Character.isLetter(c)) {
            evt.consume();
            JOptionPane.showMessageDialog(null,"la edad no puede sobrepasar los 99 aÃ±os \n No puede Ingresar Letras");
        }
    }//GEN-LAST:event_txtEdadKeyTyped

    private void txtCIKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCIKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        int ci=10;
        if((txtCI.getText().length()>=ci || Character.isLetter(c))) {
            evt.consume();
            JOptionPane.showMessageDialog(null,"solo puede ingresar 10 digitos \n Nopuede Ingresar Letras ");
        }
        
    }//GEN-LAST:event_txtCIKeyTyped

